package com.cafe.pos;

public class User {
    public String id;
    public String userName;
    public String nama;
    public String password;
    public String status;

    public User(String id, String userName, String nama, String password, String status) {
        this.id = id;
        this.userName = userName;
        this.nama = nama;
        this.password = password;
        this.status = status;
    }
}
